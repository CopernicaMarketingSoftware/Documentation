Templates are the basis for emailings you create in Copernica. You can create templates, and use them to create multiple emails.

How to create a template
------------------------

- In Copernica, navigate to *Templates*
- Go to *Template* and select *New template*
- Enter a name and description and hit *Store*

Next you can choose from three different Smarty versions. In Copernica you’ll probably use Smarty mainly to personalize documents.

You also have the option to add example code to a template. The example code is available in different languages.

After creating a template, it’ll become visible in the left column. Congratulations! You just create your first new email template!

